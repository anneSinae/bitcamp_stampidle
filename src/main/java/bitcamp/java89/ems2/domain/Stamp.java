package bitcamp.java89.ems2.domain;

public class Stamp {
  protected int stampNo;
  protected int stampIssueCount;
  protected String stampIssueDate;
  
  
public int getStampNo() {
	return stampNo;
}
public void setStampNo(int stampNo) {
	this.stampNo = stampNo;
}
public int getStampIssueCount() {
	return stampIssueCount;
}
public void setStampIssueCount(int stampIssueCount) {
	this.stampIssueCount = stampIssueCount;
}
public String getStampIssueDate() {
	return stampIssueDate;
}
public void setStampIssueDate(String stampIssueDate) {
	this.stampIssueDate = stampIssueDate;
}
  
  
}
