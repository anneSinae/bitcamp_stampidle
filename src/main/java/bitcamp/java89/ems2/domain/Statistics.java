package bitcamp.java89.ems2.domain;

public class Statistics {

  
  protected int visitCount;
  protected int cardIssueCount;
  protected int stampIssueCount;
  protected int newCustomerCount;
  protected int messageCount;
  protected int freeServiceCount;
  
  
  
  
  public int getVisitCount() {
    return visitCount;
  }
  public void setVisitCount(int visitCount) {
    this.visitCount = visitCount;
  }
  public int getCardIssueCount() {
    return cardIssueCount;
  }
  public void setCardIssueCount(int cardIssueCount) {
    this.cardIssueCount = cardIssueCount;
  }
  public int getStampIssueCount() {
    return stampIssueCount;
  }
  public void setStampIssueCount(int stampIssueCount) {
    this.stampIssueCount = stampIssueCount;
  }
  public int getNewCustomerCount() {
    return newCustomerCount;
  }
  public void setNewCustomerCount(int newCustomerCount) {
    this.newCustomerCount = newCustomerCount;
  }
  public int getMessageCount() {
    return messageCount;
  }
  public void setMessageCount(int messageCount) {
    this.messageCount = messageCount;
  }
  public int getFreeServiceCount() {
    return freeServiceCount;
  }
  public void setFreeServiceCount(int freeServiceCount) {
    this.freeServiceCount = freeServiceCount;
  }
  
}
